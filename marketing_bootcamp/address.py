class Address:
    def __init__(self, pais, estado, ciudad, numero_exterior, numero_interior, calle, codigo_postal):
        self.pais = pais
        self.estado = estado
        self.ciudad = ciudad
        self.numero_exterior = numero_exterior
        self.numero_interior = numero_interior
        self.calle = calle
        self.codigo_postal = codigo_postal
